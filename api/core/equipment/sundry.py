def get_sundries_setinfo():
    infolist = []
    for i in sundries_list.keys():
        data = {}
        data['id'] = i
        info = eval(sundries_list[i](text=True))
        num = 0
        for k in index:
            data[k] = info[num]
            num += 1
        infolist.append(data)
    return infolist
